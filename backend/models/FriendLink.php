namespace backend\models;

use yii;
use common\models\FriendLink as CommonFriendLink;
use feehi\libs\File;

class FriendLink extends CommonFriendLink
{

    public function rules()
    {
        return [
            [['target'], 'string'],
            [['sort', 'status', 'created_at', 'updated_at'], 'integer'],
            [['name', 'image', 'url'], 'string', 'max' => 255],
            [['name', 'url'], 'required'],
        ];
    }

    public function beforeSave($insert)
    {
        if($insert){
            $this->created_at = time();
        }else{
            $this->updated_at = time();
        }
        if(isset($_FILES['FriendLink']['name']['image']) && $_FILES['FriendLink']['name']['image'] != ''){
            $file = new File();
            $imgs = $file->upload(Yii::getAlias('@webroot').yii::$app->params['uploadPath']['friend-link']);
            if($imgs[0] == false){
                return false;
            }
            $this->image = str_replace(Yii::getAlias('@webroot'), '', $imgs[0]);
        }
        if($this->image == '') unset($this->image);
        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }
}